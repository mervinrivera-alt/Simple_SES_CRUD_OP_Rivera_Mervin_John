    private void table3() {
        DefaultTableModel model3 = (DefaultTableModel) jTable3.getModel();
        model3.setRowCount(0);
        String sql = "SELECT e.`Enrollment ID`, s.`First_Name`, c.`Course_Name`, e.`Enrollment_Date` \n" +
                     "FROM mydb.`enrollment table` e \n" +
                     "INNER JOIN mydb.`student Table` s ON e.`Student Table_Student Id` = s.`Student Id` \n" +
                     "INNER JOIN mydb.`course table` c ON e.`Course Table_Course_ID` = c.`Course_ID`;";

        try (Connection conn = DBConnection.getConnection(); 
             PreparedStatement stmt = conn.prepareStatement(sql); 
             ResultSet rs = stmt.executeQuery()) {

          
            while (rs.next()) {
                int enrollId = rs.getInt("Enrollment ID");
                String studentName = rs.getString("First_Name"); // Notice we ask for the String name!
                String courseName = rs.getString("Course_Name");   // Notice we ask for the String name!
                String enrollDate = rs.getString("Enrollment_Date");

                // 5. Add this row to the JTable
                model3.addRow(new Object[]{enrollId, studentName, courseName, enrollDate});
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading data: " + ex.getMessage());
        }
    }

