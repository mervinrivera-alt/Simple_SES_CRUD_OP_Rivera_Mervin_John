    private void table() {
        DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel();
        StudentCRUD sc = new StudentCRUD();
        List<StudentInfo> List = sc.getAllStudents();
        model1.setRowCount(0);
        for (StudentInfo s : List) {
            Object[] row = {
                s.getStudent_ID(),
                s.getFirst_Name(),
                s.getLast_Name(),
                s.getAge(),
                s.getEmail()
            };
            model1.addRow(row);
        }
        jTable1.revalidate();
        jTable1.repaint();
    }

